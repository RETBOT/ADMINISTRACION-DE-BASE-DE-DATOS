    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
            // Conectamos a la base de datos 
            c.ConectarBasedeDatos();
           // Obtiene ID Empleado 
            String ID = JOptionPane.showInputDialog(null,"ID Empleado: ","Eliminar Empleado",JOptionPane.QUESTION_MESSAGE);
             // Generamos la sentencia sql, con los datos ingresados del empleado
            String sql = "use Empresa DELETE FROM empleados WHERE IDEmpleado = "+ID;
            // Se ejecuta la sentencia 
            c.sentencia.execute(sql);
            // se elimina de la tabla 
            jButVer.doClick();
            // Si se ejecutÃ³ correctamente, entonces notifica al usuario
            JOptionPane.showMessageDialog(null, "Empleado Eliminado");

           // se desconecta de la base de datos para evitar errores
            c.DesconectarBasedeDatos();
        } catch (SQLException ex) {
            // En caso de tener un error, lo notifica al usuario.
            JOptionPane.showMessageDialog(null, "Error al Eliminar Empleado");
            JOptionPane.showMessageDialog(null,ex.toString());
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

